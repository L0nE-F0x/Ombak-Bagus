/** Spots shown in the app today. All catalogs are visible (no paywall yet). */
export function visibleSpots(): Spot[] {
  return BALI_SPOTS;
}

export function getSpotById(id: string): Spot | undefined {
  return BALI_SPOTS.find((s) => s.id === id);
}

export function spotsByCatalog(catalog: SpotCatalog): Spot[] {
  return BALI_SPOTS.filter((s) => s.catalog === catalog);
}

